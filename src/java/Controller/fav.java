    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String fav = request.getParameter("fav");
        String uidraw = request.getParameter("idu");
        String fidraw = request.getParameter("idf");
        String a = request.getParameter("id");
        int id = Integer.parseInt(a);
   

        UserDAO udao = new UserDAO();
        try {
            int ui = Integer.parseInt(uidraw);
            int fi = Integer.parseInt(fidraw);
            if (fav != null) {
                udao.insertFav(ui, fi);

            }
            request.getRequestDispatcher("video?" + id ).forward(request, response);
        } catch (NumberFormatException e) {
        }
    }

